using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DesafioOperacion
{
    static class Operacion
    {
        public static int sumar(int num1, int num2)
        {
            return (num1 + num2);
        }

        public static int restar(int num1, int num2)
        {
            return (num1 - num2);
        }
    }
